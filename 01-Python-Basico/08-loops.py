# Bucle For
foods = ['apples', 'bread', 'sugar', 'milk'];

for food in foods:
    if food == 'apples':
        print('tienes que comprar esto: ' + food)
        # break (Esto es para parar el bucle)
        # continue (Esto es para continuar ignorando las manzanas)
    print(food);
    

# Bucle while
